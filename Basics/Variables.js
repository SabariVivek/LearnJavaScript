/**
 * Variables are containers for storing data values...
 * 
 * We can create variables using let, var & const keywords...
 * 
 * -----
 * 
 * var --> Function Scope means, for the undefined variable, it throw exception as "Undefined"...
 * let --> Blocked scope means, for the undefined variable, it throw exception as "Error"... 
 * const --> final variable in java means value cannot be changed once declared...
 */

var a = 100;
let b = 100;
const c = 100;

console.log(a);
console.log(b);
console.log(c);