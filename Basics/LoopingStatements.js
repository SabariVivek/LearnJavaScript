/**
 * To execute same line of code multiple line, we prefer Looping Statment...
 * 
 * Types :
 *      While loop
 *      do while loop
 *      for loop
 *      for in loop -- Covered in Objects & Arrays...
 *      for of loop -- Covered in Objects & Arrays...
 */

// While Loop...
let i = 1;
while (i <= 10) {
    console.log(i);
    i++;
}

// Do While Loop...
let j = 1;
do {
    console.log("I am in Do While Loop...")
    j++;
}
while (j <= 5);

// For Loop...
for (let k = 1; k <= 5; k++) {
    console.log("I am in For Loop...")
}